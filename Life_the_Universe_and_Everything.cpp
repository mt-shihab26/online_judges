#include <bits/stdc++.h>
using namespace std;

#define endn "\n"

void test(void)
{
    int num;
    while (1)
    {
        cin >> num;
        if (num == 42)
            break;
        cout << num << endn;
    }
}

int main(void)
{
    test();
    return 0;
}
